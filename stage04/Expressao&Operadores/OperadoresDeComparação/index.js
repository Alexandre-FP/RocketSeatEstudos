// Operador de comparação

// irá comparar valores e retornar um Boolean com a resposta a comparação

let um = 1;
let dois = 2;

// ==      igual a
console.log(dois == 1);  // false
console.log(um == '1');  // true

// !=      diferente de
console.log(um != dois);  // true
console.log(um != 1);    // false
console.log(um != '1'); // false