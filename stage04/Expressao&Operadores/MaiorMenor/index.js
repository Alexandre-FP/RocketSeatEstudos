let um = 1;
let dois = 2;

// >    Maior que
console.log(um > dois);

// >=   Maior ou igual a
console.log(um >= 1); // true
console.log(dois >= 2); // true

// <    Menor que
console.log(um < dois); // true

// <=   Menor ou igual a
console.log(um <= dois); // true
console.log(um <= 1); // true
console.log(um <= 0); // false