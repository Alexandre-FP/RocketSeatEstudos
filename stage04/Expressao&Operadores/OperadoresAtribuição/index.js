// Operadores de atribuição
let x;

console.log(x); // undefined

x = 1;

console.log(x);

// adição com a atribuição
x += 2;

console.log(x);

// subtração com a atribuição
x -= 2;

console.log(x);

// multiplicação com a atribuição
x *= 2;

console.log(x);

// divisão com a atribuição
x /= 2;

console.log(x); 

// resto com a atribuição
x %= 2;

console.log(x); 

// exponenciação com a atribuição
x **= 2;

console.log(x); 