// Operadres Aritiméticos

// multiplicação
console.log(3.87 * 5);

// divição
console.log(12 / 2);

// soma
console.log(12 + 89);

// subtração
console.log(54895 - 854);

// resto da divisão
console.log(11 % 3);

// incremento
let incremento = 0;
console.log(++incremento); // aqui ele incrementa antes de chegar nessa linha e mostra o novo valor que é 1

// decremento
let decremento = 10;
decremento--
console.log(decremento);

// exponencial
console.log(3 ** 3) // 3 elevado a 3 