// Operador Condicional

// Depedendo da condição, nós receberemos valores diferentes

// Condição então o valor 1 se não 2
// condição ? value1 : value2

// Exemplos


// Café da manhã top
let pao = true;
let queijo = true;

const niceCafe = pao && queijo ? "Eu gosto do café" : "En não gosto do café"
console.log(niceCafe);

// Mior de 18
let idade = 16;
const dirigir = idade >= 18 ? "Voce pode dirigir" : "Voce nao pode dirigir";

console.log(dirigir);