// Operadores Lógicos

// 2 booelan, quando verificados, resultara em verdadeiro ou falso

let pao = true;
let queijo = false;

// AND === &&
console.log(pao && queijo); // sempre vai me retorna true se toda minha expressao foi verdadeira

// OR ||
console.log(pao || queijo);  // se uma das condições for true vai me retorna verdadeiro, mas se toda a expressao for false vai me retorna falso

// NOT !
console.log(!pao || !queijo); // vou esta negando minha variavel trocando os valores dela pode mudar depedendo da minha operação e aplicação